'use client';

import { useEffect, useState, useCallback } from 'react';
import { useParams, useRouter } from 'next/navigation';
import Link from 'next/link';

interface CronJob {
  id: string;
  name: string;
  schedule: string;
  timezone: string | null;
  description: string;
  model: string | null;
  channel: string | null;
  enabled: boolean;
  nextRun: string | null;
  sessionTarget: string;
}

function timeUntil(iso: string): string {
  const ms = new Date(iso).getTime() - Date.now();
  if (ms <= 0) return 'soon';
  const mins = Math.round(ms / 60000);
  if (mins < 60) return `in ${mins}m`;
  const hrs = Math.round(mins / 60);
  if (hrs < 24) return `in ${hrs}h`;
  return `in ${Math.floor(hrs / 24)}d`;
}

export default function JobDetailPage() {
  const { id } = useParams<{ id: string }>();
  const router = useRouter();

  const [job, setJob] = useState<CronJob | null>(null);
  const [loading, setLoading] = useState(true);
  const [saving, setSaving] = useState(false);
  const [toggling, setToggling] = useState(false);
  const [saved, setSaved] = useState(false);
  const [error, setError] = useState('');
  const [hasChanges, setHasChanges] = useState(false);

  // Editable fields
  const [name, setName] = useState('');
  const [description, setDescription] = useState('');
  const [schedule, setSchedule] = useState('');
  const [model, setModel] = useState('');
  const [enabled, setEnabled] = useState(true);

  const loadJob = useCallback(async () => {
    try {
      const res = await fetch('/api/cron');
      if (!res.ok) throw new Error('Failed to fetch');
      const data = await res.json();
      const found: CronJob | undefined = (data.jobs || []).find((j: CronJob) => j.id === id);
      if (!found) {
        setError('Job not found');
        return;
      }
      setJob(found);
      setName(found.name || '');
      setDescription(found.description || '');
      setSchedule(found.schedule || '');
      setModel(found.model || '');
      setEnabled(found.enabled !== false);
      setHasChanges(false);
    } catch (err) {
      setError(String(err));
    } finally {
      setLoading(false);
    }
  }, [id]);

  useEffect(() => { loadJob(); }, [loadJob]);

  const handleSave = async () => {
    if (!job) return;
    setSaving(true);
    setError('');
    try {
      const res = await fetch('/api/cron', {
        method: 'PATCH',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ jobId: job.id, name, description, schedule, model, enabled }),
      });
      if (!res.ok) {
        const d = await res.json();
        throw new Error(d.error || 'Save failed');
      }
      const d = await res.json();
      if (d.job) {
        setJob(d.job);
        setName(d.job.name || '');
        setDescription(d.job.description || '');
        setSchedule(d.job.schedule || '');
        setModel(d.job.model || '');
        setEnabled(d.job.enabled !== false);
      }
      setHasChanges(false);
      setSaved(true);
      setTimeout(() => setSaved(false), 2500);
    } catch (err) {
      setError(String(err));
    } finally {
      setSaving(false);
    }
  };

  const handleToggle = async () => {
    if (!job || toggling) return;
    setToggling(true);
    try {
      const res = await fetch('/api/cron', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ jobId: job.id, enabled: !enabled }),
      });
      if (res.ok) {
        const next = !enabled;
        setEnabled(next);
        setJob(prev => prev ? { ...prev, enabled: next } : null);
      }
    } finally {
      setToggling(false);
    }
  };

  const track = (setter: (v: string) => void) => (e: React.ChangeEvent<HTMLInputElement | HTMLTextAreaElement>) => {
    setter(e.target.value);
    setHasChanges(true);
  };

  if (loading) {
    return (
      <div className="min-h-screen flex items-center justify-center bg-zinc-950">
        <div className="text-zinc-600 text-sm">Loading job...</div>
      </div>
    );
  }

  if (error && !job) {
    return (
      <div className="min-h-screen flex flex-col items-center justify-center gap-4 bg-zinc-950">
        <div className="text-red-400 text-sm">{error}</div>
        <Link href="/proactivity" className="text-sm text-zinc-500 hover:text-zinc-300 transition-colors">
          Back to Proactivity
        </Link>
      </div>
    );
  }

  return (
    <div className="min-h-screen bg-zinc-950 flex flex-col">

      {/* Top bar */}
      <div className="border-b border-zinc-800 bg-zinc-900/50 px-6 py-3 flex items-center justify-between gap-4">
        <div className="flex items-center gap-4 min-w-0">
          <Link
            href="/proactivity"
            className="flex items-center gap-1.5 text-zinc-500 hover:text-zinc-300 transition-colors text-sm flex-shrink-0"
          >
            <svg className="w-4 h-4" fill="none" viewBox="0 0 24 24" stroke="currentColor">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M15 19l-7-7 7-7" />
            </svg>
            Proactivity
          </Link>
          <span className="text-zinc-700">/</span>
          <span className="text-sm text-zinc-400 truncate">
            {name || job?.id}
          </span>
        </div>

        <div className="flex items-center gap-3 flex-shrink-0">
          {/* Enabled toggle */}
          <button
            onClick={handleToggle}
            disabled={toggling}
            className="flex items-center gap-2 text-sm transition-colors"
            title={enabled ? 'Disable job' : 'Enable job'}
          >
            <span className={`text-xs font-medium transition-colors ${enabled ? 'text-green-400' : 'text-zinc-600'}`}>
              {enabled ? 'Enabled' : 'Disabled'}
            </span>
            <div className={`w-9 h-5 rounded-full relative transition-colors flex-shrink-0 ${toggling ? 'opacity-50' : ''} ${enabled ? 'bg-green-500' : 'bg-zinc-700'}`}>
              <div className={`absolute w-4 h-4 rounded-full bg-white top-0.5 transition-all ${enabled ? 'left-4' : 'left-0.5'}`} />
            </div>
          </button>

          {hasChanges && (
            <span className="text-xs text-amber-400">Unsaved changes</span>
          )}

          {saved && (
            <span className="text-xs text-green-400">Saved</span>
          )}

          <button
            onClick={handleSave}
            disabled={saving || !hasChanges}
            className="px-4 py-2 rounded-lg text-sm font-medium transition-all disabled:opacity-40 disabled:cursor-not-allowed bg-orange-500 hover:bg-orange-600 text-white"
          >
            {saving ? 'Saving...' : 'Save changes'}
          </button>
        </div>
      </div>

      {/* Main content */}
      <div className="flex flex-1 overflow-hidden">

        {/* Left panel — metadata */}
        <div className="w-72 flex-shrink-0 border-r border-zinc-800 bg-zinc-900/30 overflow-y-auto p-5 space-y-5">

          {/* Job name */}
          <div>
            <label className="block text-xs font-medium text-zinc-500 uppercase tracking-wider mb-2">Name</label>
            <input
              type="text"
              value={name}
              onChange={track(setName)}
              placeholder="Job name..."
              className="w-full px-3 py-2 bg-zinc-800 border border-zinc-700 rounded-lg text-white text-sm focus:outline-none focus:border-orange-500 transition-colors"
            />
          </div>

          {/* Schedule */}
          <div>
            <label className="block text-xs font-medium text-zinc-500 uppercase tracking-wider mb-2">Schedule</label>
            <input
              type="text"
              value={schedule}
              onChange={track(setSchedule)}
              placeholder="e.g. 0 7 * * * or Every 30m"
              className="w-full px-3 py-2 bg-zinc-800 border border-zinc-700 rounded-lg text-white text-sm font-mono focus:outline-none focus:border-orange-500 transition-colors"
            />
            <p className="text-xs text-zinc-700 mt-1.5">
              Cron expr or "Every 30m" / "Every 2h"
            </p>
          </div>

          {/* Model */}
          <div>
            <label className="block text-xs font-medium text-zinc-500 uppercase tracking-wider mb-2">Model</label>
            <input
              type="text"
              value={model}
              onChange={track(setModel)}
              placeholder="e.g. claude-sonnet-4-20250514"
              className="w-full px-3 py-2 bg-zinc-800 border border-zinc-700 rounded-lg text-white text-sm font-mono focus:outline-none focus:border-orange-500 transition-colors"
            />
          </div>

          {/* Divider */}
          <div className="border-t border-zinc-800 pt-4 space-y-3">
            <div className="text-xs font-medium text-zinc-500 uppercase tracking-wider mb-3">Info</div>

            <div className="flex items-start justify-between gap-2">
              <span className="text-xs text-zinc-600">Job ID</span>
              <span className="text-xs text-zinc-500 font-mono truncate max-w-[120px]" title={job?.id}>{job?.id}</span>
            </div>

            <div className="flex items-start justify-between gap-2">
              <span className="text-xs text-zinc-600">Session</span>
              <span className="text-xs text-zinc-400">{job?.sessionTarget || 'isolated'}</span>
            </div>

            {job?.channel && (
              <div className="flex items-start justify-between gap-2">
                <span className="text-xs text-zinc-600">Posts to</span>
                <span className="text-xs text-zinc-400">{job.channel}</span>
              </div>
            )}

            {job?.nextRun && enabled && (
              <div className="flex items-start justify-between gap-2">
                <span className="text-xs text-zinc-600">Next run</span>
                <span className="text-xs text-green-400">{timeUntil(job.nextRun)}</span>
              </div>
            )}

            {job?.timezone && (
              <div className="flex items-start justify-between gap-2">
                <span className="text-xs text-zinc-600">Timezone</span>
                <span className="text-xs text-zinc-400">{job.timezone}</span>
              </div>
            )}
          </div>
        </div>

        {/* Right panel — prompt editor */}
        <div className="flex-1 flex flex-col overflow-hidden">
          <div className="px-6 pt-5 pb-2 border-b border-zinc-800">
            <h2 className="text-xs font-medium text-zinc-500 uppercase tracking-wider">Prompt / Message</h2>
            <p className="text-xs text-zinc-700 mt-1">This is the message Clawd runs on schedule. Edit it to clarify, adjust scope, or change behaviour.</p>
          </div>

          <div className="flex-1 p-6">
            <textarea
              value={description}
              onChange={track(setDescription)}
              placeholder="Enter the prompt or message this job will run..."
              className="w-full h-full min-h-[400px] px-4 py-3 bg-zinc-900 border border-zinc-800 rounded-xl text-zinc-200 text-sm font-mono leading-relaxed focus:outline-none focus:border-orange-500/50 resize-none transition-colors"
              spellCheck={false}
            />
          </div>
        </div>
      </div>

      {/* Error bar */}
      {error && (
        <div className="fixed bottom-4 left-1/2 -translate-x-1/2 px-4 py-2 bg-red-500/10 border border-red-500/20 rounded-lg text-red-400 text-sm">
          {error}
        </div>
      )}
    </div>
  );
}
